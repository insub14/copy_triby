CREATE OR REPLACE VIEW V_PAYMENT
AS
SELECT DISTINCT(PAYMENT_NO),PAYMENT_DATE,USER_NO,TRIBY_OP_NO,PAYMENT_FINAL,STATUS FROM PAYMENT
WHERE PAYMENT_DATE IN (SELECT TRUNC(SYSDATE,'MM') +LEV -16 AS THIS_MONTH 
FROM (SELECT LEVEL AS LEV
        FROM DUAL
        CONNECT BY LEVEL < TO_CHAR(LAST_DAY(SYSDATE),'DD'))) AND STATUS='Y'; 
/
CREATE OR REPLACE VIEW V_SUM
AS
SELECT TRIBY_OP_NO,SUM(PAYMENT_FINAL)AS C_SUM
FROM V_PAYMENT
GROUP BY TRIBY_OP_NO;
/
CREATE OR REPLACE TRIGGER DEL_PAY
AFTER INSERT
ON CALCULATE
FOR EACH ROW
BEGIN
    UPDATE PAYMENT P SET STATUS = 'D' WHERE :NEW.TRIBY_OP_NO= P.TRIBY_OP_NO;
    END;
/
CREATE OR REPLACE TRIGGER AUTO
AFTER INSERT
ON MEMBER
FOR EACH ROW
BEGIN 
    IF :NEW.USER_REGISTER_DATE = SYSDATE
    THEN INSERT INTO COUPON VALUES(ABS(dbms_random.RANDOM),'가입축하',1000,SYSDATE,ADD_MONTHS(SYSDATE,1),DEFAULT,:NEW.USER_NO);
    END IF;
END;
/
CREATE OR REPLACE VIEW V_CALCULATE
AS
SELECT HOST_NO,HOST_ACCOUNTNAME,BANK_NAME,ACCOUNT,TRIBY_TITLE,TRIBY_OP_NO,C_SUM
FROM V_SUM 
JOIN T_OPTION USING(TRIBY_OP_NO)
